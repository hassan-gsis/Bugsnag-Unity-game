namespace UnityEngine.SceneManagement
{
    //
    // Summary:
    //     Used when loading a scene in a player.
    public enum LoadSceneMode
    {
        //
        // Summary:
        //     Closes all current loaded scenes and loads a scene.
        Single = 0,
        //
        // Summary:
        //     Adds the scene to the current loaded scenes.
        Additive = 1
    }
}
